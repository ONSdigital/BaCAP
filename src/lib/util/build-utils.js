export async function downloadData() {
  let csv = `"Custom area profile data for ${getName()}"\n`;
  csv += `"Source: Office for National Statistics - Census 2021"\n\n`;
  csv += `"Data generated by the Build a custom area profile tool on ${new Date().toLocaleDateString(
    "en-GB",
    { year: "numeric", month: "short", day: "numeric" }
  )}"\n`;
  csv += `"The data in this profile are aggregated from small areas on a best-fit basis, and therefore may differ slightly from other sources."\n\n`;
  csv += `"Variable","Category","${getName("capitalise")}","${get(buildstate).comparison.areanm}","Unit","${getName("capitalise")}","${get(buildstate).comparison.areanm}","Unit","Base population","Source","Geography","Time period"\n`;

  get(tables).forEach((t) => {
    let meta = topicsLookup[t.code];
    let len = meta.categories.length;
    for (let i = 0; i < len; i++) {
      csv += `"${meta.label
      }","${meta.categories[i].label
      }",${t.code == 'population_mye' ? "NA" : t.data[i].percentage ? t.data[i].value : "NA"
      },${t.code == 'population_mye' ? "NA" : t.data[len + i].percentage ? t.data[len + i].value : "NA"
      },"%","${t.data[i].percentage ? t.data[i].count : t.data[i].value
      }","${t.data[len + i].percentage ? t.data[len + i].count : t.data[len + i].value
      }","${meta.base.replace(
        "all ",
        ""
      )}","${meta.base}","${meta.source}","${geographyLookup[meta.lowestGeography]}","${meta.dateLabelLong ? meta.dateLabelLong : "2021"}"\n`;
    }
  });

  var file = new Blob([csv], { type: "text/csv" });
  download(
    file,
    `${buildstate.name ? buildstate.name.replaceAll(" ", "_") : "custom_area_data"}.csv`
  );
  let opts = buildstate.name ? { areaName: buildstate.name } : {};
  analyticsEvent({ event: "fileDownload", fileExtension: "csv", ...opts });
}

