export function filterTopics(allTopics, level, coverage) {
  return allTopics
    .filter(t=>t.inVersion.includes(get(version)))//just topics in version 2
    .filter(geographyFilter(level))
    .filter(
      (t) => !t.coverage || coverage.every((c) => t.coverage.includes(c))
    );
}

